    public const DEFAULT = 'en';

    public static function resolve(Request $request): string
    {
        $locale = (string) ($request->query('lang') ?: $request->session()->get('storefront_locale', self::DEFAULT));
        $resolved = $locale === 'th' ? 'th' : self::DEFAULT;
        $request->session()->put('storefront_locale', $resolved);

        return $resolved;
    }

    public static function copy(string $section, string $locale): array
    {
        $translations = self::all();

        return $translations[$locale][$section] ?? $translations[self::DEFAULT][$section] ?? [];
    }

